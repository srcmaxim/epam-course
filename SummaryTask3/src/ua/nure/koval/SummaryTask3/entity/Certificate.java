package ua.nure.koval.SummaryTask3.entity;

import java.util.Date;

public class Certificate {

    protected int number;
    protected Date startDate;
    protected Date expirationDate;
    protected String registeredBy;

    public Certificate(int number, Date startDate, Date expirationDate, String registeredBy) {
        this.number = number;
        this.startDate = startDate;
        this.expirationDate = expirationDate;
        this.registeredBy = registeredBy;
    }

    public Certificate() {
    }

    public int getNumber() {
        return number;
    }

    public void setNumber(int value) {
        this.number = value;
    }

    public Date getStartDate() {
        return startDate;
    }

    public void setStartDate(Date value) {
        this.startDate = value;
    }

    public Date getExpirationDate() {
        return expirationDate;
    }

    public void setExpirationDate(Date value) {
        this.expirationDate = value;
    }

    public String getRegisteredBy() {
        return registeredBy;
    }

    public void setRegisteredBy(String value) {
        this.registeredBy = value;
    }

    @Override
    public String toString() {
        return "Certificate{" +
                "number=" + number +
                ", startDate=" + startDate +
                ", expirationDate=" + expirationDate +
                ", registeredBy='" + registeredBy + '\'' +
                '}';
    }
}
